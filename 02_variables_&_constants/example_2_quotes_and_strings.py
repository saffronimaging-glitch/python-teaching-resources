# Topic: Variables and Strings
# Description: Strings must be placed inside quotation marks.
# Key idea: Text data uses quotes, but numbers usually do not.

name = "John"
school = "Primark School"
year_group = 9

print("My name is", name)
print("I go to", school)
print("I am in Year", year_group)

# Quotation marks allow us to store text:
message = "Python is fun!"

print(message)
