# Topic: Data Types
# Description: Python uses different data types to store different kinds of information.

# Integer (whole number)
age = 14

# Float (decimal number)
height = 1.65

# String (text inside quotes)
name = "Aisha"

# Boolean (True or False)
is_student = True

print("Age:", age)
print("Height:", height)
print("Name:", name)
print("Is a student:", is_student)
